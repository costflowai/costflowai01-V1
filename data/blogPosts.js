// Helper functions for blog management
export const getBlogPost = (slug) => {
  return blogPosts.find(post => post.slug === slug);
};

export const getAllBlogPosts = () => {
  return blogPosts.sort((a, b) => new Date(b.publishDate) - new Date(a.publishDate));
};

export const getFeaturedBlogPosts = () => {
  return blogPosts.filter(post => post.featured);
};

export const getBlogPostsByCategory = (category) => {
  return blogPosts.filter(post => post.category === category);
};

export const getBlogPostsByTag = (tag) => {
  return blogPosts.filter(post => post.tags.includes(tag));
};

export const getRelatedBlogPosts = (currentPost, limit = 3) => {
  const related = blogPosts.filter(post =>
    post.id !== currentPost.id &&
    (post.category === currentPost.category ||
     post.tags.some(tag => currentPost.tags.includes(tag)))
  );
  return related.slice(0, limit);
};